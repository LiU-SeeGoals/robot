/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */
  /* USER CODE BEGIN Boot_Mode_Sequence_0 */

  /* USER CODE END Boot_Mode_Sequence_0 */

  /* USER CODE BEGIN Boot_Mode_Sequence_1 */

  /* USER CODE END Boot_Mode_Sequence_1 */
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick.
   */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();
  /* USER CODE BEGIN Boot_Mode_Sequence_2 */
  /* When system initialization is finished, Cortex-M7 will release Cortex-M4 by
  means of HSEM notification */
  /*HW semaphore Clock enable*/
  __HAL_RCC_HSEM_CLK_ENABLE();
  /*Take HSEM */
  HAL_HSEM_FastTake(HSEM_ID_0);
  /*Release HSEM in order to notify the CPU2(CM4)*/
  HAL_HSEM_Release(HSEM_ID_0, 0);
  /* wait until CPU2 wakes up from stop mode */
  int32_t timeout = 0xFFFF;
  while ((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) == RESET) && (timeout-- > 0))
    ;
  if (timeout < 0) {
    Error_Handler();
  }
  /* USER CODE END Boot_Mode_Sequence_2 */

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART3_UART_Init();
  MX_TIM1_Init();
  MX_SPI1_Init();
  MX_TIM3_Init();
  MX_TIM5_Init();
  MX_TIM8_Init();
  MX_TIM12_Init();
  MX_TIM2_Init();
  MX_TIM15_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */

  I2C4_Init(); // Initialize I2C used for IMU

  LOG_Init(&huart3);
  COM_Init(&hspi1, &NRF_AVAILABLE);
#ifdef PCB_MOTOR
  NAV_Init(&htim12, &htim1, &htim15, &htim3, &htim2, &htim5, &htim8);
#else
  NAV_Init(&htim12, &htim1, &htim15, &htim3, &htim4, &htim5, &htim8);
#endif
  MOTOR_Init(&htim1);
  KICKER_Init();
  IMU_Init(&hi2c4);
  LOG_InitModule(&internal_log_mod, "MAIN", LOG_LEVEL_INFO);
  HAL_GPIO_WritePin(LED_YELLOW_GPIO_Port, LED_YELLOW_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(LED_GREEN_GPIO_Port, LED_GREEN_Pin, GPIO_PIN_SET);
  LOG_INFO("Startup finished...\r\n");
  UI_Init(&huart3);
  STATE_Init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  uint32_t now = HAL_GetTick();
  bool on = false;

  while (1) {

    STATE_Test();
    continue;

    if (main_tasks & TASK_PING) {
      main_tasks &= ~TASK_PING;
      COM_Ping();
    }

    if (main_tasks & TASK_NAV_COMMAND) {
      main_tasks &= ~TASK_NAV_COMMAND;
      NAV_HandleCommands();
    }

    if (main_tasks & TASK_DATA) {
      main_tasks &= ~TASK_DATA;
    }

    // Failsafe for when communication fails.
    if (!COM_Update() && NRF_AVAILABLE) {
      NAV_StopMovement();
      COM_RF_Reset();
    }

    if (HAL_GetTick() - now > 1000) {
      if (on) {
        HAL_GPIO_WritePin(LED_YELLOW_GPIO_Port, LED_YELLOW_Pin, GPIO_PIN_RESET);
      } else {
        HAL_GPIO_WritePin(LED_YELLOW_GPIO_Port, LED_YELLOW_Pin, GPIO_PIN_SET);
      }
      on = !on;
      now = HAL_GetTick();
    }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

